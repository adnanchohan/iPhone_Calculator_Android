package com.app.calculator.Model;

public class CalculatorLogic {

    public double addition (double a, double b){
        return a + b;
    }
    public int subtract (int a, int b){
        return a - b;
    }
    public int multiply (int a, int b){
        return a * b;
    }
    public int divide (int a, int b){
        return a / b;
    }
}
